# 각 수에 대해 자릿수 합을 구한 뒤 나누어 떨어지는지 확인
n = int(input())

# 하샤드 수란 각 자릿수의 합이 자기 자신의 약수인 수이다
# 1자리 수일때, 2자리 수일때, 3자리수일때 ..... -> while문 사용, i는 1씩 커지고 i == len(num)일떄 정지


# 범위 지정
for num in range(1, n + 1):
    # 범위 내에서 숫자들이 몇자리 수인지 구한다
    num = str(num)
    i = 0
    total = 0
    while True:
    # i를 +1씩 하면서 len(num)과 같아지면 종료    
        if i == len(num):
            break
    # 각 자릿수의 합이 자기 자신의 약수인지 확인
        else:
            total += int(num[i])
        i += 1
    
    # 자기 자신의 약수이면 출력
    if int(num) % total == 0:
        print(num)        
        
    

