# 각 수에 대해 자릿수 합을 구한 뒤 나누어 떨어지는지 확인
n = int(input())

# 하샤드 수란 각 자릿수의 합이 자기 자신의 약수인 수이다
# 1부터 n까지 순회하며, 자릿수 합으로 나누어 떨어지면 출력

# 범위 지정
for num in range(1, n + 1):
    # num을 문자열로 변환해 각 자릿수 접근 (원본 num 보존)
    num_str = str(num)

    # 자릿수 합 계산: 제너레이터 표현식으로 간결하게
    digit_sum = sum(int(d) for d in num_str)

    # 자기 자신이 자릿수 합으로 나누어 떨어지면 하샤드 수
    if num % digit_sum == 0:
        print(num)
        
    